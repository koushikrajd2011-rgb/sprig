let gameSizes = [[0,0,0],[2,3,4],[5,6,7]]
let gameCursorPosition = null
let gameValue = 0;
let SQUARE_SIZE = 0;

const WELCOME_SCREEN = 0
const LEVEL_SELECTOR = 1
const NO_CARD_SELECTED = 2
const ONE_CARD_SELECTED = 3
const GAME_OVER = 4

let gameState = WELCOME_SCREEN;
let firstCardPosition = null;
let firstCardValue = 0;
let secondCardValue = 0;
let board = [];
let free = 0;
let firstCardIndex = 0;
let secondCardIndex = 0;
let number = 0; 


function initGame()
{
  let currentLevel = levels[0]
  setMap(currentLevel)
  
  addText("Memory Game", options = { x: 5, y: 2, color: color`4` })
  addText("To move WASD ", options = { x: 4, y: 8, color: color`2` })
  addText("To select J ", options = { x: 4, y: 6, color: color`2` })
  addText("Press J ", options = { x: 6, y: 13, color: color`5` })
}

function getCardSymbol (cardValue) {
  number = board[cardValue]
  if (number === 0) {
      return null;
  } else {
    return ALPHABET[number-1];
  }    
}
  
function getCardValue(y,x) {
 let cardValue = 0;
  cardValue = ((y * SQUARE_SIZE)+x)
  return cardValue;   
}

function intValues() {

  let len = Math.floor(SQUARE_SIZE ** 2 / 2)

  for (let i = 0; i < len ; i++){
    board[i] = i + 1
    board[i + len] = i + 1
  }

  for (let i = board.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1))
    const temp = board[i]
    board[i] = board[j]
    board[j] = temp
  }
  
  if (SQUARE_SIZE % 2 !== 0) {
    board[SQUARE_SIZE ** 2 - 1] = board[len]
    board[len] = 0
  }
  console.log("second", board)
}

// Start Game
initGame()


onInput("w", () => {
  getFirst(CURSOR).y -= 1
  playTune(MOVE_SOUND)
})

onInput("s", () => {
  getFirst(CURSOR).y += 1
  playTune(MOVE_SOUND)
})

onInput("a", () => {
  getFirst(CURSOR).x -= 1
  playTune(MOVE_SOUND)
})

onInput("d", () => {
  getFirst(CURSOR).x += 1
  playTune(MOVE_SOUND)
})

onInput("j", () => {

  if (gameState === WELCOME_SCREEN) {
    currentLevel =  levels[1]
    setMap(currentLevel)
    gameState = LEVEL_SELECTOR
    addSprite(1, 1, CURSOR)
    clearText()
    addText("Level Selector ", options = { x: 3, y: 2, color: color`2` })
    
  } else if (gameState === LEVEL_SELECTOR) {
    addSprite(1, 1, CURSOR)
    clearText()
    setBackground(GRAY_BG)
    gameCursorPosition = { x: getFirst(CURSOR).x, y: getFirst(CURSOR).y }
    gameValue = gameSizes[gameCursorPosition.y][gameCursorPosition.x]
    console.log(gameValue)
    SQUARE_SIZE = gameValue
    currentLevel = levels[SQUARE_SIZE]
    setMap(currentLevel)
    
    gameState = NO_CARD_SELECTED
    addSprite(0, 0, CURSOR)
    intValues()
    
  } else if (gameState === NO_CARD_SELECTED) {
  
    firstCardPosition = { x: getFirst(CURSOR).x, y: getFirst(CURSOR).y }
    firstCardValue = getCardValue(firstCardPosition.y, firstCardPosition.x)

    if (board[firstCardValue] === 0) {
      playTune(INVALID_SOUND)

    } else {
      addSprite(firstCardPosition.x, firstCardPosition.y, SELECTED)
      gameState = ONE_CARD_SELECTED
    }

  } else if (gameState === ONE_CARD_SELECTED) {
    let secondCardPosition = { x: getFirst(CURSOR).x, y: getFirst(CURSOR).y }
    secondCardValue = getCardValue(secondCardPosition.y, secondCardPosition.x)

    if (board[secondCardValue] === 0) {
      playTune(INVALID_SOUND)
      gameState = ONE_CARD_SELECTED
    } else if (firstCardPosition.x === secondCardPosition.x && firstCardPosition.y === secondCardPosition.y){
      playTune(INVALID_SOUND)
      gameState = ONE_CARD_SELECTED

    } else {
      clearTile(firstCardPosition.x, firstCardPosition.y)
      clearTile(secondCardPosition.x, secondCardPosition.y)

      let firstCardSymbol = getCardSymbol(firstCardValue)
      let secondCardSymbol = getCardSymbol(secondCardValue)

      addSprite(firstCardPosition.x, firstCardPosition.y, firstCardSymbol)
      addSprite(secondCardPosition.x, secondCardPosition.y, secondCardSymbol)

      if (firstCardSymbol === secondCardSymbol) {
        setTimeout(() => {
            addSprite(firstCardPosition.x, firstCardPosition.y, CORRECT)
            addSprite(secondCardPosition.x, secondCardPosition.y, CORRECT)
        }, 1000)

        setTimeout(() => {
          clearTile(firstCardPosition.x, firstCardPosition.y)
          clearTile(secondCardPosition.x, secondCardPosition.y)
          addSprite(secondCardPosition.x, secondCardPosition.y, CURSOR)
          if (board.every(val => val === 0)) {
            clearTile(secondCardPosition.x, secondCardPosition.y)
            gameState = GAME_OVER
            console.log(gameState)
            addText("You Win!", options = { x: 6, y: 5, color: color`5` })
            addText("Press J to ", options = { x: 5, y: 10, color: color`0` })
            addText("PLAY AGAIN! ", options = { x: 5, y: 12, color: color`0` })
          }
        }, 2000)

        board[firstCardValue] = 0;
        board[secondCardValue] = 0;

        console.log(board)
        playTune(CORRECT_SOUND)

      } else {
        setTimeout(() => {
          addSprite(firstCardPosition.x, firstCardPosition.y, INCORRECT)
          addSprite(secondCardPosition.x, secondCardPosition.y, INCORRECT)
        }, 1000)

        setTimeout(() => {
          addSprite(firstCardPosition.x, firstCardPosition.y, BACKCARD)
          addSprite(secondCardPosition.x, secondCardPosition.y, BACKCARD)
          addSprite(secondCardPosition.x, secondCardPosition.y, CURSOR)
        }, 2000)

        playTune(INCORRECT_SOUND)
      }

      gameState = NO_CARD_SELECTED
    }
  } else if (gameState === GAME_OVER) {
      gameState = WELCOME_SCREEN
      clearText()
      initGame()

  }
})

